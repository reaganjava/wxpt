USE WXPT;
CREATE TABLE `business_question` (
    `QUID` int(11) NOT NULL AUTO_INCREMENT,
    `GOODS_ID` int(11) DEFAULT NULL,
    `SHOP_ID` int(11) DEFAULT NULL,
    `UID` bigint(20) NOT NULL,
    `NICKNAME` varchar(30) NOT NULL,
    `CONTENT` varchar(200) NOT NULL,
    `DATE` datetime NOT NULL,
    `STATUS` int(11) NOT NULL,
    PRIMARY KEY (`QUID`),
    KEY `QUESTION_IDNEX` (`QUID`),
    KEY `QUESTION_GOODS_INDEX` (`GOODS_ID`),
    KEY `QUESTION_UID_INDEX` (`UID`)
)  ENGINE=InnoDB DEFAULT CHARSET=utf8$$

CREATE  TABLE `business_question_answer` (
  `ANID` INT NOT NULL AUTO_INCREMENT ,
  `QUID` INT NOT NULL ,
  `ADMINID` INT NOT NULL ,
  `USERNAME` VARCHAR(20) NOT NULL ,
  `DATE` DATETIME NOT NULL ,
  `IS_GOOD` INT NOT NULL ,
  `CONTENT` VARCHAR(200) NOT NULL ,
  PRIMARY KEY (`ANID`) ,
  INDEX `ANSWER_INDEX` (`ANID` ASC) ,
  INDEX `ANSWER_QUESTION_INDEX` (`QUID` ASC) ,
  INDEX `ANSWER_ADMIN_INDEX` (`ADMINID` ASC) )
DEFAULT CHARACTER SET = utf8;

