USE WXPT;
CREATE  TABLE `common_group` (
  `GROUPID` INT NOT NULL AUTO_INCREMENT ,
  `COMPANY_ID` INT NOT NULL ,
  `NAME` VARCHAR(30) NOT NULL ,
  `CREATENAME` VARCHAR(20) NOT NULL ,
  `CREATEDATE` DATETIME NOT NULL ,
  PRIMARY KEY (`GROUPID`) ,
  INDEX `GROUP_INDEX` (`GROUPID` ASC) ,
  INDEX `GROUP_COMPANY_INDEX` (`COMPANY_ID` ASC) ,
  CONSTRAINT `GROUP_COMPANY_PK`
    FOREIGN KEY (`COMPANY_ID` )
    REFERENCES `wxpt`.`business_company` (`COID` )
    ON DELETE NO ACTION
    ON UPDATE NO ACTION)
ENGINE=InnoDB DEFAULT CHARSET=utf8;

CREATE  TABLE IF NOT EXISTS `wxpt`.`common_menu_item` (
  `MENUID` INT(11) NOT NULL AUTO_INCREMENT ,
  `COMPANY_ID` INT(11) NOT NULL ,
  `GROUP_ID` INT(11) NOT NULL ,
  `TITLE` VARCHAR(20) NOT NULL ,
  `URL` VARCHAR(100) NOT NULL ,
  `CREATENAME` VARCHAR(20) NOT NULL ,
  `CREATEDATE` DATETIME NOT NULL ,
  `ICON` VARCHAR(100) NOT NULL ,
  `STATUS` INT(11) NOT NULL ,
  `PRIORITY` INT(11) NOT NULL ,
  PRIMARY KEY (`MENUID`) ,
  INDEX `menu_company_index` (`COMPANY_ID` ASC) ,
  INDEX `menu_index` (`MENUID` ASC) ,
  INDEX `menu_gorup_index` (`GROUP_ID` ASC) ,
  CONSTRAINT `MENU_COMPANY_FK`
    FOREIGN KEY (`COMPANY_ID` )
    REFERENCES `wxpt`.`business_company` (`COID` )
    ON DELETE NO ACTION
    ON UPDATE NO ACTION)
ENGINE = InnoDB
DEFAULT CHARACTER SET = utf8;
