USE WXPT;
CREATE  TABLE `business_comment` (
  `CMID` INT NOT NULL AUTO_INCREMENT ,
  `CM_TOPIC_ID` INT NOT NULL DEFAULT 0 ,
  `COMPANY_ID` INT NOT NULL ,
  `ANSWER_AID` INT NULL ,
  `UID` BIGINT NOT NULL ,
  `CONTENT` VARCHAR(200) NOT NULL ,
  `NICKNAME` VARCHAR(30) NOT NULL ,
  `USERNAME` VARCHAR(20) NULL ,
  `DATE` DATETIME NOT NULL ,
  `STATUS` INT NOT NULL ,
  `IS_GOOD` INT NOT NULL ,
  PRIMARY KEY (`CMID`) ,
  INDEX `COMMENT_IDNEX` (`CMID` ASC) ,
  INDEX `COMMENT_TOPIC_IDNEX` (`CM_TOPIC_ID` ASC) ,
  INDEX `COMMENT_COPMANY_INDEX` (`COMPANY_ID` ASC) ,
  INDEX `COMMENT_UID_INDEX` (`UID` ASC) ,
  CONSTRAINT `COMMON_COMPANY_PK`
    FOREIGN KEY (`COMPANY_ID` )
    REFERENCES `business_company` (`COID` )
    ON DELETE NO ACTION
    ON UPDATE NO ACTION,
  CONSTRAINT `COMMON_MEMBER_PK`
    FOREIGN KEY (`UID` )
    REFERENCES `common_members` (`UID` )
    ON DELETE NO ACTION
    ON UPDATE NO ACTION
  CONSTRAINT `COMMON_TOPIC_PK`
	  FOREIGN KEY (`CM_TOPIC_ID` )
	  REFERENCES `business_comment` (`CMID` )
	  ON DELETE NO ACTION
	  ON UPDATE NO ACTION)
ENGINE = InnoDB
DEFAULT CHARACTER SET = utf8;
